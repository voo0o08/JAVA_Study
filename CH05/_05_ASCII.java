package CH05;

public class _05_ASCII {
    public static void main(String[] args) {
        // 아스키 코드 ANSI 미국 표준 코드
        char c = 'A'; // A = 65 / a = 97 / 0 = 48
        System.out.println(c); // A
        System.out.println((int)c); // 65

        c = 'B';
        System.out.println(c); // B
        System.out.println((int)c); // 66

    }
}
